<?php

namespace HomeBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Asdf
 *
 * @ORM\Table(name="asdf")
 * @ORM\Entity
 */
class Asdf
{
    /**
     * @var integer
     *
     * @ORM\Column(name="asdf", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $asdf;



    /**
     * Get asdf
     *
     * @return integer
     */
    public function getAsdf()
    {
        return $this->asdf;
    }
}
